#include"libft.h"

int	ft_isalnum(int c)
{
	if (('a' <= c && 'z' >= c) || ('A' <= c && 'Z' >= c)
		|| (c >= 48 && c <= 57))
	{
		return (1);
	}
	return (0);
}
